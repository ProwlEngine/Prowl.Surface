namespace NWindows.Input;

/// <summary>
/// The mouse buttons.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// No defined.
    /// </summary>
    None = 0,

    /// <summary>
    /// The left button.
    /// </summary>
    Left,

    /// <summary>
    /// The middle button.
    /// </summary>
    Middle,

    /// <summary>
    /// The right button.
    /// </summary>
    Right,

    /// <summary>
    /// The XButton1.
    /// </summary>
    XButton1,

    /// <summary>
    /// The XButton2.
    /// </summary>
    XButton2,
}